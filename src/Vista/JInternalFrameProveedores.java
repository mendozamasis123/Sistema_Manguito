    public void obtenerDatos() {
    List<Proveedores> proveedores = new DAOProveedores().ObtenerDatos();
    DefaultTableModel modelo = new DefaultTableModel();
    String[] columnas = {"Id_Proveedores", "Cedula", "Nombre", "Email"};
    modelo.setColumnIdentifiers(columnas);
    
    for (Proveedores Pro : proveedores) { 
        String[] renglon = {
            Integer.toString(Pro.getId_proveedor()),
            Pro.getCedula_proveedor(),
            Pro.getNombre_proveedor(),
            Pro.getCorreo_electronico(),
        };
        modelo.addRow(renglon);
    }
    jTableProveedores.setModel(modelo); 
}
    public void actualizarProveedores() {
    int Id_Prov = Integer.parseInt(this.jTextid_proveedor.getText());
    String cedP = this.jTextcedula_proveedor.getText();
    String nomP = this.jTextnombre_proveedor.getText();
    String emP = this.jTextcorreo_electronico.getText();


    DAOProveedores dao = new DAOProveedores();
    int res = dao.Actualizar(Id_Prov, cedP, nomP, emP);
    if (res == 1) {
        JOptionPane.showMessageDialog(rootPane, "proveedor Actualizado!");
    } else {
        JOptionPane.showMessageDialog(rootPane, "¡Ocurrió un ERROR!");
    }
}

