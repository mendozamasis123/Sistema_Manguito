    private void jBAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAgregarActionPerformed
        String ced = jTextcedula_proveedor.getText();
        String nomb = jTextnombre_proveedor.getText();
        String ema = jTextcorreo_electronico.getText();

        if (ced.contentEquals("") || nomb.contentEquals("") || ema.contentEquals("")) {

            JOptionPane.showMessageDialog(rootPane, "Todos los campos son obligatorios");
        }else{
            try {
                Proveedores pro = new DAOProveedores().Insertar(ced, nomb, ema);
                JOptionPane.showMessageDialog(rootPane, "Registro agregado");
            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(rootPane, "No se agregó el registro");
            }

            obtenerDatos();
            limpiarCampos();

        }
    }//GEN-LAST:event_jBAgregarActionPerformed

    private void jBEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBEditarActionPerformed
        int fila = this.jTableProveedores.getSelectedRow(); // Se obtiene #fila seleccionada
        if (fila == -1) {
            JOptionPane.showMessageDialog(rootPane, "Seleccione un registro de la tabla");
        } else { // Se toma cada campo de la tabla del registro seleccionado
            try {
                int id = Integer.parseInt((String) this.jTableProveedores.getValueAt(fila, 0).toString());
                String ced = (String) this.jTableProveedores.getValueAt(fila, 1);
                String nomb = (String) this.jTableProveedores.getValueAt(fila, 2);
                String ema = (String) this.jTableProveedores.getValueAt(fila, 3);

                // Se ubican en las cajas de textos los datos capturados de la tabla
                jTextid_proveedor.setText("" + id);
                jTextcedula_proveedor.setText(ced);
                jTextnombre_proveedor.setText(nomb);
                jTextcorreo_electronico.setText(ema);

            } catch (NumberFormatException e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jBEditarActionPerformed

