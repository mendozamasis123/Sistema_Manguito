    private void jBAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAgregarActionPerformed
String nomb = jTextNombreP.getText();
int can = Integer.parseInt(jTextCantidad.getText());
String pre = jTextPresentacion.getText();
float prec = Float.parseFloat(jTextPrecio.getText());

if (nomb.contentEquals("") || can <= 0 || pre.contentEquals("") ||
    prec <= 0) {
        
    JOptionPane.showMessageDialog(rootPane, "Todos los campos son obligatorios");
}else{
    try {
    Producto pro = new DAOProducto().Insertar(nomb, can, pre, prec);
    JOptionPane.showMessageDialog(rootPane, "Registro agregado");
} catch (Exception e) {
    e.printStackTrace();
    JOptionPane.showMessageDialog(rootPane, "No se agregó el registro");
}
    
obtenerDatos();
limpiarCampos();

}
    }//GEN-LAST:event_jBAgregarActionPerformed

    private void jBEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBEditarActionPerformed
        int fila = this.jTableProducto.getSelectedRow(); // Se obtiene #fila seleccionada
if (fila == -1) {
    JOptionPane.showMessageDialog(rootPane, "Seleccione un registro de la tabla");
} else { // Se toma cada campo de la tabla del registro seleccionado
    try {
        int id = Integer.parseInt((String) this.jTableProducto.getValueAt(fila, 0).toString());
        String nomb = (String) this.jTableProducto.getValueAt(fila, 1);
        int can = Integer.parseInt((String) this.jTableProducto.getValueAt(fila, 2));
        String pre = (String) this.jTableProducto.getValueAt(fila, 3);
        float prec = Float.parseFloat((String) this.jTableProducto.getValueAt(fila, 4));

        // Se ubican en las cajas de textos los datos capturados de la tabla
        jTextIdProducto.setText("" + id);
        jTextNombreP.setText(nomb);
        jTextCantidad.setText(String.valueOf(can));
        jTextPresentacion.setText(pre);
        jTextPrecio.setText(String.valueOf(prec));
    } catch (NumberFormatException e) {
        e.printStackTrace();
    }
}
    }//GEN-LAST:event_jBEditarActionPerformed

