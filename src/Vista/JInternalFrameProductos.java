     public void obtenerDatos() {
    List<Producto> producto = new DAOProducto().ObtenerDatos();
    DefaultTableModel modelo = new DefaultTableModel();
    String[] columnas = {"id_producto", "nombre_producto", "cantidad_disponible",
        "presentacion", "precio"};
    modelo.setColumnIdentifiers(columnas);
    
    for (Producto pro : producto) { 
        String[] renglon = {
            Integer.toString(pro.getId_producto()),
            pro.getNombre_producto(),
            Integer.toString(pro.getCantidad_disponible()),
            pro.getPresentacion(),
            Float.toString(pro.getPrecio())

        };
        modelo.addRow(renglon);
    }
    jTableProducto.setModel(modelo); 
}
     public void actualizarProducto() {
    int id = Integer.parseInt(this.jTextIdProducto.getText());
    String nom = this.jTextNombreP.getText();
    int can = Integer.parseInt(this.jTextCantidad.getText());
    String pre = this.jTextPresentacion.getText();
    float prec = Float.parseFloat(this.jTextPrecio.getText());

    DAOProducto dao = new DAOProducto();
    int res = dao.Actualizar(id, nom, can, pre, prec);
    if (res == 1) {
        JOptionPane.showMessageDialog(rootPane, "Producto Actualizado!");
    } else {
        JOptionPane.showMessageDialog(rootPane, "¡Ocurrió un ERROR!");
    }
}

